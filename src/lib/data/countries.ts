// Helper function to get cities by country name
export const getCitiesByCountry = (countryName: string): string[] => {
  const country = AMERICAN_COUNTRIES.find(
    (c) => c.name.toLowerCase() === countryName.toLowerCase(),
  );
  return country ? country.cities : [];
};

// Helper function to get country by code
export const getCountryByCode = (code: string): Country | undefined => {
  return AMERICAN_COUNTRIES.find((c) => c.code === code);
};

// Helper function to search countries
export const searchCountries = (query: string): Country[] => {
  if (!query || query.length < 2) return [];

  return AMERICAN_COUNTRIES.filter(
    (country) =>
      country.name.toLowerCase().includes(query.toLowerCase()) ||
      country.cities.some((city) =>
        city.toLowerCase().includes(query.toLowerCase()),
      ),
  );
};
